"""CLI demo: end-to-end IB generation on the synthetic XYZ-001 corpus.

This runs the full pipeline:
  1. LocalFileConnector ingests samples/synthetic_compound/sources/
  2. The parser registry chunks every document
  3. ReportGenerator runs plan -> fill -> critique against the ICH E6 IB
     template using a "smart stub" LLM client (no Vertex access required)
  4. The result is dumped to samples/synthetic_compound/output/ib_run.json

To run with the real Vertex AI Claude (once you have ADC + project access),
pass `--vertex-project` and `--vertex-region`; the script swaps the stub for
VertexLlmClient automatically.

Usage:
    # With the stub (no GCP needed):
    python samples/run_synthetic_ib.py

    # With Vertex AI Claude (after `gcloud auth application-default login`):
    python samples/run_synthetic_ib.py --vertex-project my-gsk-project --vertex-region us-east5

Outputs: samples/synthetic_compound/output/ib_run.json
"""

